algo = input('Digite algo: ')

print('É numerico? ', algo.isnumeric())
print('É alfanumérico? ', algo.isalnum())
print('Está todo em caixa alta? ', algo.isupper())
print('Está toddo em caixa baixa? ', algo.islower())
print('Está capitalizado? ', algo.istitle())
print('É imprimivel? ', algo.isprintable())